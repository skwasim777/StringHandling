package SubString;

public class Test01 {
	public static void main(String[] args) {
		String s1 = "SachinTendulkar";
		System.out.println("Original String " + s1);
		System.out.println("SubString Starting from index 6 =" + s1.substring(6));
		System.out.println("SubString Starting from index 0 to 6 =" + s1.substring(0, 6));
	}
}
